namespace Z0
{
    using System;

    /// <summary>
    /// Defines literals that correspond to base-2 digits
    /// </summary>
    public enum BinaryDigitSymbol : ushort
    {
        /// <summary>
        /// Specifies 0 base 2, asci code 48
        /// </summary>
        Zed = '0',
        
        /// <summary>
        /// Specifies 1 base 2, asci code 49
        /// </summary>
        One = '1'
    }
}