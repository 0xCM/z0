namespace Z0
{
    [SymbolSource]
    public enum AsciSymSymbol : ushort
    {
        /// <summary>
        /// The '&' character
        /// </summary>
        Amp = '&',

        /// <summary>
        /// The '.' character
        /// </summary>
        Dot = '.',

        /// <summary>
        /// The '@' character
        /// </summary>
        At = '@',

        /// <summary>
        /// The ',' character
        /// </summary>
        Comma = ',',

        /// <summary>
        /// The '$' character
        /// </summary>
        Dollar = '$',

        /// <summary>
        /// The '#' character
        /// </summary>
        Hash = '#',

        /// <summary>
        /// The '|' character
        /// </summary>
        Pipe = '|',

        /// <summary>
        /// The '?' character
        /// </summary>
        Question = '?',

        /// <summary>
        /// The '_' character
        /// </summary>
        US = '_',

        /// <summary>
        /// The ' ' character
        /// </summary>
        Space = ' ',

        /// <summary>
        /// The '\\' symbol
        /// </summary>
        BS = '\\',

        /// <summary>
        /// The '\/' symbol
        /// </summary>
        FS = '/',

        /// The '{' symbol
        /// </summary>
        LBrace = '{',

        /// <summary>
        /// The '}' symbol
        /// </summary>
        RBrace = '}',

        /// <summary>
        /// The '[' symbol
        /// </summary>
        LB = '[',

        /// <summary>
        /// The ']' symbol
        /// </summary>
        RB = ']',

        /// <summary>
        /// The '(' symbol
        /// </summary>
        LParen = '(',

        /// <summary>
        /// The ')' symbol
        /// </summary>
        RParen = ')',

        /// <summary>
        /// The ':' character
        /// </summary>
        Colon = ':',

        /// <summary>
        /// The ';' character
        /// </summary>
        Semicolon = ';',

        /// <summary>
        /// The '"' (double-quote) character
        /// </summary>
        DQuote = '\"',

        /// <summary>
        /// The ''' (single-quote) character
        /// </summary>
        SQuote = '\'',
    }
}