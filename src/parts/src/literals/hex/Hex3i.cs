namespace Z0
{
    using System;

    /// <summary>
    /// Defines signed identifiers corresponding to the hex digits 0,..,7
    /// </summary>
    public enum Hex3i : sbyte
    {
        /// <summary> 
        /// Identifies the hex value 0x00 := 0
        /// </summary> 
        x0i = 0x0,

        /// <summary> 
        /// Identifies the hex value 0x01 := 1
        /// </summary> 
        x1i = 0x1,

        /// <summary> 
        /// Identifies the hex value 0x02 := 2
        /// </summary> 
        x2i = 0x2,

        /// <summary> 
        /// Identifies the hex value 0x03 := 3
        /// </summary> 
        x3i = 0x3,

        /// <summary> 
        /// Identifies the hex value 0x04 := 4
        /// </summary> 
        x4i = 0x4,

        /// <summary> 
        /// Identifies the hex value 0x05 := 5
        /// </summary> 
        x5i = 0x5,

        /// <summary> 
        /// Identifies the hex value 0x06 := 6
        /// </summary> 
        x6i = 0x6,

        /// <summary> 
        /// Identifies the hex value 0x07 := 7
        /// </summary> 
        x7i = 0x7,
    }    
}