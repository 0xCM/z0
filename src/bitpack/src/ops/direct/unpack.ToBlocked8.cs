        /// <param name="block">The block index</param>
        /// <typeparam name="T">The source type</typeparam>
        [MethodImpl(Inline), Op]
        public static ref readonly Block512<byte> unpack(ulong src, in Block512<byte> dst, int block = 0)        
        {
