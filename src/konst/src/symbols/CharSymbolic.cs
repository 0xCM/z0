namespace Z0
{    
    using C = Chars;

    public enum CharSymbolic : ushort
    {
        /// <summary>
        /// The '&' character
        /// </summary>
        Amp = C.Amp,

        /// <summary>
        /// The '@' character
        /// </summary>
        At = C.At,

        /// <summary>
        /// The '!' character
        /// </summary>
        Bang = C.Bang,

        /// <summary>
        /// The backslash character
        /// </summary>
        BSlash = C.BSlash,

        /// <summary>
        /// The '^' character
        /// </summary>
        Caret = C.Caret,

        /// <summary>
        /// The ':' character
        /// </summary>
        Colon = C.Colon,
    }
}