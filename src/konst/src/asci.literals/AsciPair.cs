namespace Z0
{    
    public enum AsciPair : ushort
    {        
        /// <summary>
        /// The '{' symbol
        /// </summary>
        BraceL = '{',

        /// <summary>
        /// The '}' symbol
        /// </summary>
        BraceR = '}',

        /// <summary>
        /// The '[' symbol
        /// </summary>
        BrackL = '[',

        /// <summary>
        /// The ']' symbol
        /// </summary>
        BrackR = ']',

        /// <summary>
        /// The '(' symbol
        /// </summary>
        ParenL = '(',        

        /// <summary>
        /// The ')' symbol
        /// </summary>
        ParenR = ')',                

        /// <summary>
        /// The ':' character
        /// </summary>
        Colon = ':',

        /// <summary>
        /// The ';' character
        /// </summary>
        ColonS = ';',

        /// <summary>
        /// The '"' (double-quote) character
        /// </summary>
        QuoteD = '\"',

        /// <summary>
        /// The ''' (single-quote) character 
        /// </summary>
        QuoteS = '\'',
    }
}