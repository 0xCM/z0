namespace Z0
{
    using System;
    using System.Runtime.CompilerServices;
    using System.Diagnostics;

    using static Konst;

    partial struct z
    {                        
        /// <summary>
        /// Captures a stopwatch duration
        /// </summary>
        /// <param name="sw">A running/stopped stopwatch</param>
        [MethodImpl(Inline)]   
        Duration snapshot(Stopwatch sw)
            => Duration.Define(sw.ElapsedTicks);                
    }
}
