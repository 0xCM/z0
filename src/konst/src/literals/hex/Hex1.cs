namespace Z0
{
    using System;
    
    /// <summary>
    /// Defines identifiers corresponding to the hex digits 0 and 1
    /// </summary>
    public enum Hex1 : byte
    {
        /// <summary> 
        /// Identifies the hex value 0x00 := 0
        /// </summary> 
        x0 = 0x0,

        /// <summary> 
        /// Identifies the hex value 0x01 := 1
        /// </summary> 
        x1 = 0x1,
    }
}