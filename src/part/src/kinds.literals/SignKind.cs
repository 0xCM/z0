namespace Z0
{
    /// <summary>
    /// Defines polarity classifiers
    /// </summary>
    public enum SignKind : sbyte
    {
        /// <summary>
        /// Specifies negative polarity
        /// </summary>
        Negative = -1,

        /// <summary>
        /// Specifies neutral polarity
        /// </summary>
        Neutral = 0,

        /// <summary>
        /// Specifies positive polarity
        /// </summary>
        Positive = 1
    }
}