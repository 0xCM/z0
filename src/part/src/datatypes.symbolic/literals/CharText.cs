namespace Z0
{    
    /// <summary>
    /// Defines an arbitrary collection of text literals
    /// </summary>
    public static class CharText
    {
        /// <summary>
        /// The ':' character text
        /// </summary>
        public const string Colon = ":";
     
        /// <summary>
        /// The '=' character text
        /// </summary>
        public const string Eq = "=";

        /// <summary>
        /// The '|' character text
        /// </summary>
        public const string Pipe = "|";

        /// The ' ' character text
        /// </summary>
        public const string Space = " ";

        /// <summary>
        /// The ''' character text
        /// </summary>
        public const string SQuote = "\'";
    
    }
}