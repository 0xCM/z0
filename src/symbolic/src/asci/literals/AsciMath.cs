    using System;

    public enum AsciMath : ushort
    {        
        /// <summary>
        /// The '=' character
        /// </summary>
        EQ = '=',

        /// <summary>
        /// The '>' symbol
        /// </summary>
        GT = '>',

        /// <summary>
        /// The ',' symbol
        /// </summary>
        LT = '<',

        /// <summary>
        /// The '^' character
        /// </summary>
        EXP = '^',

        /// <summary>
        /// The '+' character
        /// </summary>
        ADD = '+',

        /// <summary>
        /// The '-' character
        /// </summary>
        SUB = '-',

        /// <summary>
        /// The '*' character
        /// </summary>
        MUL = '*',

        /// <summary>
        /// The '/' symbol
        /// </summary>
        DIV = '/',

        /// <summary>
        /// The '%' character
        /// </summary>
        MOD = '%',

        /// <summary>
        /// The '~' character
        /// </summary>
        SYM = '~', 

        /// <summary>
        /// The '!' character
        /// </summary>
        NOT = '!',
    }
}