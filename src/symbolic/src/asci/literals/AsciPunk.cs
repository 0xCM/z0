namespace Z0
{
    using System;

    public enum AsciPunk : ushort
    {
        /// <summary>
        /// The '&' character
        /// </summary>
        Amp = '&',

        /// <summary>
        /// The '.' character
        /// </summary>
        Dot = '.',

        /// <summary>
        /// The '@' character
        /// </summary>
        At = '@',

        /// <summary>
        /// The ',' character
        /// </summary>
        Comma = ',',

        /// <summary>
        /// The '$' character
        /// </summary>
        Dollar = '$',

        /// <summary>
        /// The '#' character
        /// </summary>
        Hash = '#',

        /// <summary>
        /// The '|' character
        /// </summary>
        Pipe = '|',

        /// <summary>
        /// The '?' character
        /// </summary>
        QU = '?',

        /// <summary>
        /// The '_' character
        /// </summary>
        US = '_', 

        /// <summary>
        /// The ' ' character
        /// </summary>
        Space = ' ',

        /// <summary>
        /// The '\\' symbol
        /// </summary>
        BS = '\\',
    }
}