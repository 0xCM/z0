namespace Z0
{        
    using System;

    /// <summary>
    /// Defines literals corresponding to the set of unique 3-bit sequences
    /// </summary>
    public enum Triad : byte
    {
        b000 = 0b000,

        b001 = 0b001,

        b010 = 0b010,

        b011 = 0b011,

        b100 = 0b100,

        b101 = 0b101,

        b110 = 0b110,

        b111 = 0b111,

    }        
}