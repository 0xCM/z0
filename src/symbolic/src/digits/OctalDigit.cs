namespace Z0
{
    using System;

    /// <summary>
    /// Defines literals corresponding to base-8 digits
    /// </summary>
    public enum OctalDigit : byte
    {
        /// <summary>
        /// Specifies 0 base 8
        /// </summary>
        D0 = 0x0,

        /// <summary>
        /// Specifies 1 base 8
        /// </summary>
        D1 = 0x1,
        
        /// <summary>
        /// Specifies 2 base 8
        /// </summary>
        D2 = 0x2,
        
        /// <summary>
        /// Specifies 3 base 8
        /// </summary>
        D3 = 0x3,

        /// <summary>
        /// Specifies 4 base 8
        /// </summary>
        D4 = 0x4,

        /// <summary>
        /// Specifies 5 base 8
        /// </summary>
        D5 = 0x5,

        /// <summary>
        /// Specifies 6 base 8
        /// </summary>
        D6 = 0x6,

        /// <summary>
        /// Specifies 7 base 8
        /// </summary>
        D7 = 0x7,        
   }
}